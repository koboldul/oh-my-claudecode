function normalizeNewlines(value) {
  return value.replace(/^\uFEFF/, '').replace(/\r\n/g, '\n');
}

function semanticScanTokens(value) {
  return normalizeNewlines(value)
    .toLowerCase()
    .replace(/[^\p{L}\p{N}]+/gu, ' ')
    .trim()
    .split(/\s+/)
    .filter(Boolean)
    .flatMap((token) => {
      if (token === 'builtin') return ['built', 'in'];
      if (token === 'teams') return ['team'];
      if (token === 'natively') return ['native'];
      if (token === 'implicitly') return ['implicit'];
      return [token];
    });
}

function containsUnorderedTerms(tokens, terms, windowSize) {
  for (let start = 0; start < tokens.length; start += 1) {
    const window = tokens.slice(start, start + windowSize);
    if (terms.every((term) => window.includes(term))) return true;
  }
  return false;
}

