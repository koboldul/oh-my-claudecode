function parsePrompt(source, sourceName) {
  const normalized = normalizeNewlines(source);
  const match = normalized.match(/^---\n([\s\S]*?)\n---(?:\n([\s\S]*))?$/);
  if (!match) {
    throw new Error(`Prompt ${sourceName} has no YAML frontmatter`);
  }
  return {
    frontmatter: match[1],
    body: match[2] ?? '',
  };
}

function frontmatterValue(frontmatter, key) {
  const match = frontmatter.match(
    new RegExp(`^${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}:\\s*(.*)$`, 'm'),
  );
  return match?.[1]?.trim() ?? null;
}

function parseAliases(frontmatter) {
  const raw = frontmatterValue(frontmatter, 'aliases');
  if (!raw || raw === '[]') return [];
  if (raw.startsWith('[') && raw.endsWith(']')) {
    return raw
      .slice(1, -1)
      .split(',')
      .map((value) => value.trim().replace(/^["']|["']$/g, ''))
      .filter(Boolean);
  }
  return [raw.replace(/^["']|["']$/g, '')];
}

