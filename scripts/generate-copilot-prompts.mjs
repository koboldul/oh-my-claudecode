function replaceSemanticTokens(value, replacements) {
  let output = value;
  for (const [from, to] of [...replacements].sort(
    ([left], [right]) => right.length - left.length,
  )) {
    output = output.split(from).join(to);
  }
  return output;
}

function isIdentifierBoundary(value) {
  return value === undefined || !/[A-Za-z0-9_-]/.test(value);
}

function findMatchingParen(value, openIndex) {
  let depth = 0;
  let quote = null;
  let escaped = false;

  for (let index = openIndex; index < value.length; index += 1) {
    const char = value[index];
    if (quote) {
      if (escaped) escaped = false;
      else if (char === '\\') escaped = true;
      else if (char === quote) quote = null;
      continue;
    }
    if (char === '"' || char === "'") {
      quote = char;
      continue;
    }
    if (char === '(') depth += 1;
    else if (char === ')') {
      depth -= 1;
      if (depth === 0) return index;
    }
  }
  return -1;
}

function splitCallArguments(value) {
  const args = [];
  let start = 0;
  let depth = 0;
  let quote = null;
  let escaped = false;

  for (let index = 0; index < value.length; index += 1) {
    const char = value[index];
    if (quote) {
      if (escaped) escaped = false;
      else if (char === '\\') escaped = true;
      else if (char === quote) quote = null;
      continue;
    }
    if (char === '"' || char === "'") {
      quote = char;
      continue;
    }
    if (char === '(' || char === '[' || char === '{') depth += 1;
    else if (char === ')' || char === ']' || char === '}') depth -= 1;
    else if (char === ',' && depth === 0) {
      args.push(value.slice(start, index).trim());
      start = index + 1;
    }
  }
  const final = value.slice(start).trim();
  if (final) args.push(final);
  return args;
}

function rewriteNamedCalls(value, names, rewrite) {
  const orderedNames = [...names].sort((left, right) => right.length - left.length);
  let output = '';
  let cursor = 0;

  while (cursor < value.length) {
    let matchedName = null;
    for (const name of orderedNames) {
      if (
        value.startsWith(name, cursor) &&
        isIdentifierBoundary(value[cursor - 1]) &&
        value[cursor + name.length] === '('
      ) {
        matchedName = name;
        break;
      }
    }

    if (!matchedName) {
      output += value[cursor];
      cursor += 1;
      continue;
    }

    const openIndex = cursor + matchedName.length;
    const closeIndex = findMatchingParen(value, openIndex);
    if (closeIndex < 0) {
      output += value[cursor];
      cursor += 1;
      continue;
    }

    const args = value.slice(openIndex + 1, closeIndex);
    output += rewrite(matchedName, args);
    cursor = closeIndex + 1;
  }

  return output;
}

