function applyTransforms(value, requestedTransforms) {
  const requested = new Set(requestedTransforms);
  for (const transform of requested) {
    if (!TRANSFORMERS.has(transform)) {
      throw new Error(`Unsupported Copilot prompt transform: ${transform}`);
    }
  }

  let output = value;
  for (const transform of TRANSFORM_ORDER) {
    if (requested.has(transform)) {
      output = TRANSFORMERS.get(transform)(output);
    }
  }
  return output;
}

function cleanTemplate(value) {
  const lines = normalizeNewlines(value).replace(/^\n/, '').split('\n');
  while (lines.length > 0 && lines.at(-1).trim() === '') lines.pop();
  const indentation = lines
    .filter((line) => line.trim())
    .map((line) => line.match(/^\s*/)[0].length);
  const minimum = indentation.length > 0 ? Math.min(...indentation) : 0;
  return `${lines.map((line) => line.slice(minimum)).join('\n')}\n`;
}

