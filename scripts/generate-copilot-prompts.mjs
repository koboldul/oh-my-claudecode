let gitIndexModes;

function loadGitIndexModes() {
  if (gitIndexModes) return gitIndexModes;
  gitIndexModes = new Map();
  try {
    const output = execFileSync(
      'git',
      [
        'ls-files',
        '--stage',
        '-z',
        '--',
        'skills',
        'commands',
        'skills-copilot',
        'commands-copilot',
      ],
      {
        cwd: ROOT,
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'ignore'],
        windowsHide: true,
      },
    );
    for (const record of output.split('\0')) {
      if (!record) continue;
      const match = record.match(/^(\d{6}) [0-9a-f]+ \d\t(.+)$/);
      if (match) gitIndexModes.set(toPosixPath(match[2]), match[1]);
    }
  } catch {
    // npm pack runs from an isolated workspace without Git metadata.
  }
  return gitIndexModes;
}

function indexedMode(path) {
  return loadGitIndexModes().get(repoPath(path)) ?? null;
}

function normalizedFileMode(path) {
  const indexed = indexedMode(path);
  if (indexed) return indexed;
  return statSync(path).mode & 0o111 ? '100755' : '100644';
}

function supportFileMode(entry, relativePath, sourcePath) {
  const normalizedRelativePath = toPosixPath(relativePath);
  const declared =
    entry.supportFileModes?.[normalizedRelativePath] ?? '100644';
  const sourceMode = indexedMode(sourcePath);
  if (sourceMode && sourceMode !== declared) {
    throw new Error(
      `${entry.source} support mode drift for ${normalizedRelativePath}: expected ${sourceMode}, matrix declares ${declared}`,
    );
  }
  return declared;
}

